create table schedule_templates (
  id bigint generated by default as identity primary key,
  user_profile_id uuid references user_profiles(id) on delete set null,
  schedule_name text not null,
  publish_time time not null,
  timezone text not null default 'Asia/Kolkata',
  repeat_type app_schedule_repeat_type not null,
  end_date date,
  custom_cron text,
  is_active boolean not null default true,
  last_used_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint schedule_templates_custom_cron_required check (
    repeat_type <> 'custom' or nullif(trim(custom_cron), '') is not null
  ),
  constraint schedule_templates_onetime_end_date_required check (
    repeat_type <> 'onetime' or end_date is not null
  )
);

