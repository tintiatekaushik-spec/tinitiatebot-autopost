create table posts (
  id text primary key,
  user_profile_id uuid references user_profiles(id) on delete set null,
  publishing_account_id text not null references publishing_accounts(id) on delete restrict,
  platform_name app_platform_name not null,
  post_title text,
  post_caption text not null,
  post_status app_post_status not null default 'queued',
  azure_container_name text,
  azure_blob_path text,
  azure_blob_url text,
  original_file_name text,
  media_file_type app_media_file_type not null default 'other',
  mime_type text,
  file_extension text,
  file_size_bytes bigint,
  scheduled_publish_at timestamptz,
  schedule_template_id bigint references schedule_templates(id) on delete set null,
